public class Book_Store {
	public String title;
	public String author;
	public int year;
	public int isbn;
	public double price;
	
	public Book_Store(String title,String author,int year,int isbn, double price){
		this.title = title;
		this.author = author;
		this.year = year;
		this.isbn = isbn;
		this.price = price;
	}
	
	
}
